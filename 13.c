#include<stdio.h>

int main()
{
    int N;

    printf("ODD EVEN NUMBER EVALUATOR\n\n");


    printf("Enter any Number");
    scanf("%d",&N);

    if(N%2 == 0)
    {

        printf("IT IS A EVEN NUMBER\a");
    }
    else
    {
        printf("IT IS A ODD NUMBER\a");
    }

    return 0;




}
